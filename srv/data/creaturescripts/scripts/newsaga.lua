function addSagaOutfit(cid, newLevel)
if isPlayer(cid) then
local VOC = getPlayerVocation(cid)
local STOR = (VOC+12000)
local NEXT_SAGA = getPlayerStorageValue(cid,STOR)+1
if saga[VOC] and NEXT_SAGA <= #saga[VOC] then
local NEXT_SAGA_LEVEL = saga[VOC][NEXT_SAGA]
if newLevel >= NEXT_SAGA_LEVEL  then
doPlayerSendTextMessage(cid, MESSAGE_INFO_DESCR, "Você atingiu o nivel "..NEXT_SAGA_LEVEL.." e desbloqueou uma nova Saga!")
setPlayerStorageValue(cid, STOR, NEXT_SAGA)
doPlayerSave(cid, true)
end
end
end
end

function onAdvance(cid, skill, oldLevel, newLevel)
if skill == 8 then
addSagaOutfit(cid, newLevel)
end
return true
end
