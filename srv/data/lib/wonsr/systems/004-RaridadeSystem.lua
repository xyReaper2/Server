function doAddContainerItemRandom(cid, premios, gold)
local file = "data/logs/RaridadeSystem.txt"
local text = io.open(file, "a+")
local bag = doCreateItemEx(2595)
local str = ""

if premios then
    for _, itemid in pairs (premios) do			
        local newItem = doRandomItem(math.random(1,10000),itemid)
        str = str .. ", "..getItemNameById(newItem)..""
        doAddContainerItem(bag, newItem, 1)
        if newItem ~= itemid then
        doBroadcastMessage("The player "..getCreatureName(cid).." won a "..getItemNameById(newItem).."")
        text:write("["..os.date("%d/%m/%Y %X", os.time()).."] The player "..getCreatureName(cid).." ["..getPlayerLevel(cid).."] won a "..getItemNameById(newItem).."\n")
        end
    end
end

if gold and gold > 0 then
doAddContainerItem(bag, 2160, gold)
str = str .. " and "..gold.." iens"
end
text:close()
setPlayerStorageValue(cid, 942134, str)
return doPlayerSendMailByName(getCreatureName(cid), bag, 2)
end


